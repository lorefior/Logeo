package com.curso.escuela.logeo.com.curso.escuela.logeo;

public class Profesor {
	private String nombre;
	private String apelllido;
	private Integer catedra;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getApelllido() {
		return apelllido;
	}
	public void setApelllido(String apelllido) {
		this.apelllido = apelllido;
	}
	public Integer getCatedra() {
		return catedra;
	}
	public void setCatedra(Integer catedra) {
		this.catedra = catedra;
	}
}
